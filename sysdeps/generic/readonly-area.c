/* Return 1 if the whole area PTR .. PTR+SIZE is not writable.
   Return -1 if it is writable.  */

#include <stdlib.h>

int
__readonly_str (const void *ptr, size_t size)
{
  /* The conservative answer is that all strings are writable.  */
  return -1;
}
